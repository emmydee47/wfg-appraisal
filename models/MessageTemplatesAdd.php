        // created_at
        $this->created_at->CurrentValue = CurrentDate();
        $this->created_at->setDbValueDef($rsnew, $this->created_at->CurrentValue, null);

        // updated_at
        $this->updated_at->CurrentValue = CurrentDate();
        $this->updated_at->setDbValueDef($rsnew, $this->updated_at->CurrentValue, CurrentDate());

        // created_by
        $this->created_by->CurrentValue = CurrentUserID();
        $this->created_by->setDbValueDef($rsnew, $this->created_by->CurrentValue, null);

        // Call Row Inserting event
        $insertRow = $this->rowInserting($rsold, $rsnew);
        if ($insertRow) {
            $addRow = $this->insert($rsnew);
            if ($addRow) {
            }
        } else {
            if ($this->getSuccessMessage() != "" || $this->getFailureMessage() != "") {
                // Use the message, do nothing
            } elseif ($this->CancelMessage != "") {
                $this->setFailureMessage($this->CancelMessage);
                $this->CancelMessage = "";
            } else {
                $this->setFailureMessage($Language->phrase("InsertCancelled"));
            }
            $addRow = false;
        }
        if ($addRow) {
            // Call Row Inserted event
            $this->rowInserted($rsold, $rsnew);
        }

        // Clean upload path if any
        if ($addRow) {
        }

        // Write JSON for API request
        if (IsApi() && $addRow) {
            $row = $this->getRecordsFromRecordset([$rsnew], true);
            WriteJson(["success" => true, $this->TableVar => $row]);
        }
        return $addRow;
    }

